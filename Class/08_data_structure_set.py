'''
३. Set (सेट)
Set एक unordered (अक्रमबद्ध) र unindexed (असङ्कलित) डाटा प्रकार हो जसमा 
अद्वितीय (unique) आइटमहरू मात्र हुन्छन्। Sets {} (ब्रेसेस) भित्र लेखिन्छन्।
'''
numbers = {1,2,3,4,5}
print(numbers)

# Set मा आइटम थप्ने
numbers.add(6)
print(numbers)

# Set मा आइटम हटाउने
numbers.remove(1)
print(numbers)
