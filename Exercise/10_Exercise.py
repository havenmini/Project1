# प्रयोगकर्ताबाट एउटा संख्या लिई त्यो संख्याको गुणनखण्डहरू प्रिन्ट गर्ने प्रोग्राम लेख्नुहोस्।

# प्रयोगकर्ताबाट संख्या लिने
number = int(input("कृपया एउटा संख्या प्रविष्ट गर्नुहोस्: "))

print(f"{number} को गुणनखण्डहरू:")

# गुणनखण्डहरू पत्ता लगाउने
for i in range(1, number + 1):
    if number % i == 0:
        print(i)
